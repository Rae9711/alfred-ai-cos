export function greetingForLocale(hour: number, locale: Locale): string {
  const g = translations[locale].greeting;
  if (hour < 5) return g.stillUp;
  if (hour < 12) return g.morning;
  if (hour < 18) return g.afternoon;
  return g.evening;
}
